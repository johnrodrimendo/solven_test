package com.affirm.bantotalrest.model.common;

public class SdtDatosPEP {

    private String entidad;
    private String cargoRelacion;
    private String observacion;
    private String fechaInicio;
    private String fechaSalida;

    public String getEntidad() {
        return entidad;
    }

    public void setEntidad(String entidad) {
        this.entidad = entidad;
    }

    public String getCargoRelacion() {
        return cargoRelacion;
    }

    public void setCargoRelacion(String cargoRelacion) {
        this.cargoRelacion = cargoRelacion;
    }

    public String getObservacion() {
        return observacion;
    }

    public void setObservacion(String observacion) {
        this.observacion = observacion;
    }

    public String getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(String fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public String getFechaSalida() {
        return fechaSalida;
    }

    public void setFechaSalida(String fechaSalida) {
        this.fechaSalida = fechaSalida;
    }
}
