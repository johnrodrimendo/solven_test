        aztecaGetawayBase.setCodigoClienteExterno(getCODIGO() != null ? getCODIGO().toString() : null);
        aztecaGetawayBase.setTipoCliente(getCampos_Adicional_2() != null && !getCampos_Adicional_2().isEmpty() ? Integer.valueOf(getCampos_Adicional_2()) : null);
