package com.affirm.warmi.model;

public class HealthSystem {

    private String insuredType;
    private String systemType;
    private String validThru;
    private String affiliateNumberSis;
    private String insuredTypeSis;
    private String insuredStatusSis;

    public String getInsuredType() {
        return insuredType;
    }

    public void setInsuredType(String insuredType) {
        this.insuredType = insuredType;
    }

    public String getSystemType() {
        return systemType;
    }

    public void setSystemType(String systemType) {
        this.systemType = systemType;
    }

    public String getValidThru() {
        return validThru;
    }

    public void setValidThru(String validThru) {
        this.validThru = validThru;
    }

    public String getAffiliateNumberSis() {
        return affiliateNumberSis;
    }

    public void setAffiliateNumberSis(String affiliateNumberSis) {
        this.affiliateNumberSis = affiliateNumberSis;
    }

    public String getInsuredTypeSis() {
        return insuredTypeSis;
    }

    public void setInsuredTypeSis(String insuredTypeSis) {
        this.insuredTypeSis = insuredTypeSis;
    }

    public String getInsuredStatusSis() {
        return insuredStatusSis;
    }

    public void setInsuredStatusSis(String insuredStatusSis) {
        this.insuredStatusSis = insuredStatusSis;
    }
}
