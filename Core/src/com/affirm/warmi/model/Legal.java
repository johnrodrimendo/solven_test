package com.affirm.warmi.model;

public class Legal {

    private String resolutionNumber;
    private String resolutionDate;
    private String resolutionParts;

    public String getResolutionNumber() {
        return resolutionNumber;
    }

    public void setResolutionNumber(String resolutionNumber) {
        this.resolutionNumber = resolutionNumber;
    }

    public String getResolutionDate() {
        return resolutionDate;
    }

    public void setResolutionDate(String resolutionDate) {
        this.resolutionDate = resolutionDate;
    }

    public String getResolutionParts() {
        return resolutionParts;
    }

    public void setResolutionParts(String resolutionParts) {
        this.resolutionParts = resolutionParts;
    }
}
