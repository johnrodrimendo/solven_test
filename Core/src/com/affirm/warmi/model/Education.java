package com.affirm.warmi.model;

public class Education {

    private String degreeName;
    private String degreeRegisterDate;
    private String degreeInstituteName;

    public String getDegreeName() {
        return degreeName;
    }

    public void setDegreeName(String degreeName) {
        this.degreeName = degreeName;
    }

    public String getDegreeRegisterDate() {
        return degreeRegisterDate;
    }

    public void setDegreeRegisterDate(String degreeRegisterDate) {
        this.degreeRegisterDate = degreeRegisterDate;
    }

    public String getDegreeInstituteName() {
        return degreeInstituteName;
    }

    public void setDegreeInstituteName(String degreeInstituteName) {
        this.degreeInstituteName = degreeInstituteName;
    }
}
